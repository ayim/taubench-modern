"""BIRD benchmark test generation utilities.

This module provides reusable functions for generating BIRD benchmark test threads
and golden CSV files from BIRD benchmark data.

The PostgreSQL database is provided via the BIRD Docker Compose stack.
Golden CSVs are generated by executing SQLite gold SQL against SQLite databases.

Reference: https://github.com/AlibabaResearch/DAMO-ConvAI/tree/main/bird
Hugging Face: https://huggingface.co/birdsql/datasets
"""

from __future__ import annotations

import csv
import json
import os
import sqlite3
from pathlib import Path

import httpx
import sqlparse


def load_bird_questions(json_path: str | Path, db_id: str | None = None) -> list[dict]:
    """Load BIRD questions from a JSON file.

    Args:
        json_path: Path to BIRD questions JSON file (e.g., bird_mini_dev_sqlite.json)
        db_id: Optional database ID to filter questions (e.g., "california_schools")

    Returns:
        List of question dictionaries, optionally filtered by db_id
    """
    with open(json_path) as f:
        data = json.load(f)

    if db_id:
        questions = [q for q in data if q.get("db_id") == db_id]
    else:
        questions = data

    return questions


def execute_golden_sql(db_path: str | Path, sql: str) -> tuple[list[str], list[tuple]]:
    """Execute SQL against a SQLite database and return results.

    Args:
        db_path: Path to SQLite database file
        sql: SQL query to execute

    Returns:
        Tuple of (column_names, results)

    Raises:
        Exception: If SQL execution fails
    """
    conn = sqlite3.connect(str(db_path))
    cursor = conn.cursor()

    try:
        cursor.execute(sql)
        results = cursor.fetchall()
        col_names = [desc[0] for desc in cursor.description] if cursor.description else []
        conn.close()
        return col_names, results
    except Exception as e:
        conn.close()
        raise Exception(f"Failed to execute SQL: {e}\nSQL: {sql}") from e


def save_golden_csv(output_path: Path, col_names: list[str], results: list[tuple]) -> None:
    """Save query results as a CSV file.

    Args:
        output_path: Path where CSV file should be saved
        col_names: List of column names for the header
        results: List of result tuples to write as rows
    """
    with open(output_path, "w", newline="") as f:
        writer = csv.writer(f, lineterminator="\n")
        writer.writerow(col_names)
        writer.writerows(results)


def read_bird_context_from_csvs(db_description_dir: Path) -> str:
    """Read CSV files from database_description directory and format as business context.

    The CSV files contain column metadata including descriptions and value descriptions
    that provide business context for the LLM to use when enhancing the SDM.

    Args:
        db_description_dir: Path to database_description directory containing CSV files

    Returns:
        Formatted context string for the generate endpoint's description field
    """
    if not db_description_dir.exists():
        return ""

    csv_files = list(db_description_dir.glob("*.csv"))
    if not csv_files:
        return ""

    context_parts = []
    context_parts.append("Business Context from BIRD Benchmark Database Metadata:")
    context_parts.append("")

    for csv_file in sorted(csv_files):
        # Skip .Zone.Identifier files
        if ".Zone.Identifier" in csv_file.name:
            continue

        table_name = csv_file.stem  # Get filename without extension
        context_parts.append(f"## Table: {table_name}")
        context_parts.append("")

        try:
            with open(csv_file, newline="", encoding="utf-8") as f:
                reader = csv.DictReader(f)
                rows = list(reader)

                if not rows:
                    continue

                context_parts.append("Columns:")
                for row in rows:
                    original_col = row.get("original_column_name", "")
                    col_name = row.get("column_name", original_col)
                    col_desc = row.get("column_description", "")
                    data_format = row.get("data_format", "")
                    value_desc = row.get("value_description", "")

                    # Skip rows marked as "Not useful"
                    if value_desc and "not useful" in value_desc.lower():
                        continue

                    # Build column description
                    parts = []
                    if col_name:
                        parts.append(f"- **{col_name}**")
                    if col_desc:
                        parts.append(f" ({col_desc})")
                    if data_format:
                        parts.append(f" - Type: {data_format}")
                    if value_desc:
                        # Clean up value description
                        value_desc_clean = value_desc.strip().replace("\n", " ")
                        parts.append(f" - {value_desc_clean}")

                    if parts:
                        context_parts.append("".join(parts))

                context_parts.append("")

        except Exception as e:
            print(f"  ⚠ Warning: Failed to read {csv_file.name}: {e}")
            continue

    return "\n".join(context_parts)


async def generate_bird_sdm(
    server_url: str,
    db_id: str,
    context_dir: Path,
    output_dir: Path,
    agent_id: str,
) -> None:
    """Generate a BIRD SDM by orchestrating HTTP calls to agent server APIs.

    This function:
    0. Clones the preinstalled agent with an OpenAI platform config
    1. Ensures the BIRD PostgreSQL data connection exists
    2. Determines which tables belong to this database (from CSV files)
    3. Inspects only those tables to get metadata
    4. Reads business context from CSV files
    5. Generates the SDM with LLM enhancement
    6. Saves SDM to server to get an ID
    7. Exports SDM as properly formatted YAML
    8. Saves exported YAML to output directory
    (finally) Cleans up the cloned agent

    Args:
        server_url: Base URL of the agent server
        db_id: Database ID (e.g., "california_schools")
        context_dir: Path to database_description directory with CSV files
        output_dir: Directory where SDM files will be saved (e.g., test-data/sdms/bird_california_schools)
        agent_id: Agent ID of the base preinstalled agent to clone
    """
    import uuid

    from agent_platform.quality.models import Platform

    server_url = server_url.rstrip("/")
    clone_agent_id: str | None = None

    # Get BIRD PostgreSQL connection config from environment
    pg_host = os.getenv("BIRD_PG_HOST", "localhost")
    pg_port = int(os.getenv("BIRD_PG_PORT", "5433"))
    pg_database = os.getenv("BIRD_PG_DATABASE", "bird")
    pg_user = os.getenv("BIRD_PG_USER", "postgres")
    pg_password = os.getenv("BIRD_PG_PASSWORD", "postgres")

    connection_name = "postgres-spar"
    sdm_name = f"bird_{db_id}"

    print(f"\n🔧 Generating SDM for {db_id}...")

    # Use a long timeout - SDM generation with LLM enhancement can take 10+ minutes for large DBs
    async with httpx.AsyncClient(timeout=httpx.Timeout(600.0, connect=30.0)) as client:
        # Step 0: Clone preinstalled agent with OpenAI platform config
        # (Preinstalled agents have no platform configs, so we must clone and configure one)
        print("  0/6 Cloning agent with OpenAI platform config...")
        try:
            # Fetch base agent
            response = await client.get(f"{server_url}/api/v2/agents/{agent_id}/raw")
            response.raise_for_status()
            base_agent = response.json()

            # Create clone with unique name
            unique_suffix = str(uuid.uuid4())[:8]
            clone_name = f"bird-sdm-gen-{db_id}-{unique_suffix}"

            # Get OpenAI platform config
            openai_platform = Platform(name="openai")
            platform_config = openai_platform.as_platform_config()

            clone_payload = {
                "name": clone_name,
                "description": f"Temporary clone for BIRD SDM generation ({db_id})",
                "agent_architecture": base_agent.get("agent_architecture"),
                "structured_runbook": base_agent.get("runbook_structured"),
                "mode": base_agent.get("mode", "conversational"),
                "extra": base_agent.get("extra", {}),
                "action_packages": base_agent.get("action_packages", []),
                "mcp_servers": base_agent.get("mcp_servers", []),
                "mcp_server_ids": base_agent.get("mcp_server_ids", []),
                "agent_settings": base_agent.get("agent_settings", {}),
                "advanced_config": base_agent.get("advanced_config", {}),
                "metadata": base_agent.get("metadata", {}),
                "observability_configs": base_agent.get("observability_configs", []),
                "question_groups": base_agent.get("question_groups", []),
                "selected_tools": base_agent.get("selected_tools", {}),
                "platform_configs": [platform_config],  # Add OpenAI platform config
                "version": base_agent.get("version"),
            }

            create_resp = await client.post(f"{server_url}/api/v2/agents/", json=clone_payload)
            create_resp.raise_for_status()
            clone_agent_id = create_resp.json()["agent_id"]
            print(f"     ✓ Created clone: {clone_agent_id}")
        except Exception as e:
            print(f"     ❌ Failed to clone agent: {e}")
            raise

        try:
            # Step 1: Ensure data connection exists
            print(f"  1/6 Checking data connection '{connection_name}'...")

            # Check if connection already exists
            response = await client.get(f"{server_url}/api/v2/data-connections/")
            response.raise_for_status()
            existing_connections = response.json()

            connection_id = None
            for conn in existing_connections:
                if conn.get("name") == connection_name and conn.get("engine") == "postgres":
                    # Check if config matches
                    config = conn.get("configuration", {})
                    if (
                        config.get("host") == pg_host
                        and int(config.get("port", 0)) == pg_port
                        and config.get("database") == pg_database
                    ):
                        connection_id = conn.get("id")
                        print(f"     ✓ Using existing connection: {connection_id}")
                        break

            # Create connection if it doesn't exist
            if not connection_id:
                print("     Creating new data connection...")
                payload = {
                    "name": connection_name,
                    "description": "BIRD benchmark PostgreSQL database",
                    "engine": "postgres",
                    "configuration": {
                        "host": pg_host,
                        "port": pg_port,
                        "database": pg_database,
                        "user": pg_user,
                        "password": pg_password,
                        "schema": "public",
                        "sslmode": "prefer",
                    },
                }
                response = await client.post(f"{server_url}/api/v2/data-connections/", json=payload)
                response.raise_for_status()
                connection_data = response.json()
                connection_id = connection_data.get("id")
                print(f"     ✓ Created connection: {connection_id}")

            # Step 2: Determine which tables belong to this database
            # The database_description CSVs tell us which tables are part of this database
            print("  2/7 Determining tables for this database...")
            db_tables: list[str] = []
            if context_dir.exists():
                for csv_file in context_dir.glob("*.csv"):
                    # Skip Zone.Identifier files
                    if ".Zone.Identifier" in csv_file.name:
                        continue
                    # PostgreSQL lowercases unquoted identifiers, so we lowercase table names
                    table_name = csv_file.stem.lower()  # e.g., "postHistory.csv" -> "posthistory"
                    db_tables.append(table_name)

            if not db_tables:
                print(f"     ⚠ No table CSVs found in {context_dir}, will inspect all tables")
            else:
                print(f"     ✓ Found {len(db_tables)} tables: {', '.join(sorted(db_tables))}")

            # Step 3: Inspect data connection to get table metadata
            print("  3/7 Inspecting database tables...")
            # Format tables_to_inspect as list of TableToInspect objects
            tables_to_inspect = None
            if db_tables:
                tables_to_inspect = [
                    {"name": table_name, "database": None, "schema": "public"} for table_name in db_tables
                ]
            inspect_payload = {
                "tables_to_inspect": tables_to_inspect,
                "inspect_columns": True,
                "n_sample_rows": 5,
            }
            response = await client.post(
                f"{server_url}/api/v2/data-connections/{connection_id}/inspect",
                json=inspect_payload,
            )
            response.raise_for_status()
            inspect_response = response.json()
            tables = inspect_response.get("tables", [])
            print(f"     ✓ Inspected {len(tables)} tables")

            # Convert inspect response to tables_info format for generate endpoint
            tables_info = []
            for table in tables:
                table_info = {
                    "name": table.get("name"),
                    "database": table.get("database"),
                    "schema": table.get("schema"),
                    "description": table.get("description"),
                    "columns": table.get("columns", []),
                }
                tables_info.append(table_info)

            # Step 4: Read business context from CSV files
            print("  4/8 Reading business context from CSVs...")
            business_context = read_bird_context_from_csvs(context_dir)
            if business_context:
                print(f"     ✓ Loaded context from {len(list(context_dir.glob('*.csv')))} CSV files")
            else:
                print(f"     ⚠ No CSV context found in {context_dir}")

            # Step 5: Call generate endpoint with LLM enhancement (using cloned agent)
            print("  5/8 Generating SDM with LLM enhancement...")
            print("     (This may take several minutes...)")
            generate_payload = {
                "name": sdm_name,
                "description": business_context if business_context else f"BIRD benchmark database for {db_id}",
                "data_connections_info": [
                    {
                        "data_connection_id": connection_id,
                        "tables_info": tables_info,
                    }
                ],
                "files_info": [],
                "agent_id": clone_agent_id,  # Use cloned agent with platform config
            }

            try:
                response = await client.post(
                    f"{server_url}/api/v2/semantic-data-models/generate",
                    json=generate_payload,
                )
                response.raise_for_status()
            except httpx.HTTPStatusError as e:
                print(f"     ❌ SDM generation failed: {e.response.status_code}")
                print(f"     Response: {e.response.text[:500]}")
                raise
            except httpx.TimeoutException:
                print("     ❌ SDM generation timed out (>5 minutes)")
                print("     Try running with a smaller database or check server logs")
                raise

            generate_response = response.json()
            semantic_model = generate_response.get("semantic_model")
            print(f"     ✓ Generated SDM with {len(semantic_model.get('tables', []))} tables")

            # Step 6: Save SDM to server to get an ID
            print("  6/8 Saving SDM to server...")
            save_payload = {"semantic_model": semantic_model}
            response = await client.post(
                f"{server_url}/api/v2/semantic-data-models/",
                json=save_payload,
            )
            response.raise_for_status()
            save_response = response.json()
            sdm_id = save_response.get("semantic_data_model_id")
            print(f"     ✓ Saved SDM: {sdm_id}")

            # Step 7: Export SDM as properly formatted YAML
            print("  7/8 Exporting SDM as YAML...")
            response = await client.get(f"{server_url}/api/v2/semantic-data-models/{sdm_id}/export")
            response.raise_for_status()
            export_response = response.json()
            yaml_content = export_response.get("content")
            print("     ✓ Exported SDM")

            # Step 8: Save exported YAML to files
            print(f"  8/8 Saving SDM files to {output_dir}...")
            output_dir.mkdir(parents=True, exist_ok=True)

            # Save sdm.yml (using exported content, not raw dump)
            sdm_path = output_dir / "sdm.yml"
            with open(sdm_path, "w", encoding="utf-8") as f:
                f.write(yaml_content)
            print(f"     ✓ Wrote {sdm_path}")

            # Save config.yml
            config_path = output_dir / "config.yml"
            config_content = f"""# BIRD {db_id.replace("_", " ").title()} PostgreSQL Connection Config
#
# This config points to the BIRD Docker Compose stack which contains
# all BIRD benchmark databases in a single PostgreSQL instance.
#
# To start the BIRD stack:
#   quality-test bird docker up
#
# Environment variables (with defaults):
#   BIRD_PG_HOST=localhost
#   BIRD_PG_PORT=5433
#   BIRD_PG_DATABASE=bird
#   BIRD_PG_USER=postgres
#   BIRD_PG_PASSWORD=postgres

data_connection:
  engine: postgres
  name: {connection_name}
  description: {db_id.replace("_", " ").title()} BIRD benchmark database
  host: ${{BIRD_PG_HOST:-localhost}}
  port: ${{BIRD_PG_PORT:-5433}}
  database: ${{BIRD_PG_DATABASE:-bird}}
  user: ${{BIRD_PG_USER:-postgres}}
  password: ${{BIRD_PG_PASSWORD:-postgres}}
  schema: public
"""
            with open(config_path, "w", encoding="utf-8") as f:
                f.write(config_content)
            print(f"     ✓ Wrote {config_path}")

        finally:
            # Cleanup: Delete cloned agent
            if clone_agent_id:
                print("  Cleaning up temporary agent clone...")
                try:
                    await client.delete(f"{server_url}/api/v2/agents/{clone_agent_id}")
                    print(f"     ✓ Deleted clone: {clone_agent_id}")
                except Exception as e:
                    print(f"     ⚠ Failed to delete clone: {e}")

    print("✅ SDM generation complete!")


class BirdDatasetGenerator:
    """Generator for BIRD benchmark test threads and golden CSVs.

    This class orchestrates the generation of test threads and golden CSV files
    from BIRD benchmark data. PostgreSQL database is provided via Docker Compose.
    """

    def __init__(
        self,
        db_path: Path,
        questions_json_path: Path,
        output_threads_dir: Path,
        db_id: str,
        test_name_prefix: str,
        sdm_name: str,
    ):
        """Initialize the BIRD dataset generator.

        Args:
            db_path: Path to SQLite database file (for golden CSV generation)
            questions_json_path: Path to BIRD questions JSON file
            output_threads_dir: Directory where test threads will be created
            db_id: Database ID to filter questions (e.g., "california_schools")
            test_name_prefix: Prefix for test directory names (e.g., "bird-california-schools")
            sdm_name: Name for the SDM package (e.g., "bird_california_schools")
        """
        self.db_path = db_path
        self.questions_json_path = questions_json_path
        self.output_threads_dir = output_threads_dir
        self.db_id = db_id
        self.test_name_prefix = test_name_prefix
        self.sdm_name = sdm_name

    def generate_thread_yaml(
        self,
        question: dict,
        test_number: int,
        sdm_path: str,
    ) -> str:
        """Generate thread.yml content for a BIRD question.

        Args:
            question: Question dictionary from BIRD dataset
            test_number: Sequential test number for directory naming
            sdm_path: Path to SDM package (relative to test-data)

        Returns:
            YAML content as a string
        """
        question_id = question["question_id"]
        question_text = question["question"]
        evidence = question.get("evidence", "")
        difficulty = question.get("difficulty", "unknown")
        sql = question.get("SQL", "")

        # For description fields (single-line), escape quotes and replace newlines with spaces
        question_text_single_line = question_text.replace("\n", " ").replace('"', '\\"')

        # Format content block with proper indentation for YAML block scalar (10 spaces)
        # Each line of the question needs to be indented
        question_lines = question_text.split("\n")
        question_block = "\n          ".join(question_lines)

        content_parts = [question_block]
        if evidence:
            evidence_lines = evidence.split("\n")
            evidence_text = "\n          ".join(evidence_lines)
            content_parts.append(f"Evidence: {evidence_text}")

        content_block = "\n          ".join(content_parts)

        # Format the reference SQL query
        reference_sql_block = """# Reference SQL Query (SQLite Dialect)
#
# This is the gold-standard SQL query from the BIRD benchmark dataset.
# NOTE: This SQL is written in SQLite dialect and is used to generate the golden CSV.
# The actual test execution uses PostgreSQL, so the agent must generate PostgreSQL-compatible SQL.
reference_sql: |
  {indented_sql}
"""
        if sql:
            try:
                # Format SQL with proper indentation and uppercase keywords
                formatted_sql = sqlparse.format(
                    sql,
                    reindent=True,
                    keyword_case="upper",
                    indent_width=2,
                )
            except Exception:
                # If formatting fails, use the raw SQL
                formatted_sql = sql
            # Indent each line for YAML literal block (2 spaces)
            sql_lines = formatted_sql.split("\n")
            indented_sql = "\n  ".join(sql_lines)
            reference_sql_block = reference_sql_block.format(indented_sql=indented_sql)

        yaml_content = f"""name: {self.test_name_prefix}-{test_number:03d}
description: "BIRD Q{question_id} ({difficulty}): {question_text_single_line}"
difficulty: {difficulty}
timeout_seconds: 600

{reference_sql_block}
sdms:
  - kind: postgres
    sdm_path: {sdm_path}
    description: {self.db_id.replace("_", " ").title()} BIRD benchmark database

thread:
  - name: bird-question-{question_id}
    description: "{question_text_single_line}"
    messages:
      - role: user
        content: |
          {content_block}

target-platforms:
  - name: openai
  - name: azure
  - name: bedrock
  - name: groq

evaluations:
  - kind: dataframe-golden-comparison
    expected:
      golden_file: golden_{question_id}.csv
      match_mode: values_only
      relative_tolerance: 0.01
      strict_columns: false
    description: >
      Validate that the resulting dataframe matches the golden dataset.
      BIRD EX-style comparison (value sets only, ignoring column names).
"""
        return yaml_content

    def generate_test_threads(self, skip_existing: bool = True) -> tuple[int, int]:
        """Generate test threads with golden CSVs.

        Args:
            skip_existing: If True, skip directories that already exist

        Returns:
            Tuple of (total_generated, total_skipped)
        """
        # Load questions
        print(f"Loading BIRD questions for {self.db_id}...")
        questions = load_bird_questions(self.questions_json_path, db_id=self.db_id)
        print(f"  Found {len(questions)} questions")

        self.output_threads_dir.mkdir(parents=True, exist_ok=True)

        generated = 0
        skipped = 0

        # Generate test threads
        for idx, question in enumerate(questions, start=1):
            question_id = question["question_id"]
            sql = question["SQL"]

            # Create test directory
            test_dir_name = f"{self.test_name_prefix}-{idx:03d}"
            test_dir = self.output_threads_dir / test_dir_name

            if test_dir.exists() and skip_existing:
                print(f"\n[{idx}/{len(questions)}] Skipping Q{question_id} (directory exists)")
                skipped += 1
                continue

            test_dir.mkdir(parents=True, exist_ok=True)

            print(f"\n[{idx}/{len(questions)}] Processing Q{question_id}...")
            print(f"  Directory: {test_dir_name}")

            # Generate and save thread.yml
            yaml_content = self.generate_thread_yaml(
                question,
                idx,
                sdm_path=f"sdms/{self.sdm_name}",
            )
            yaml_path = test_dir / "thread.yml"
            yaml_path.write_text(yaml_content)
            print("  ✓ Wrote thread.yml")

            # Execute golden SQL and save results
            try:
                col_names, results = execute_golden_sql(self.db_path, sql)
                golden_csv_path = test_dir / f"golden_{question_id}.csv"
                save_golden_csv(golden_csv_path, col_names, results)
                print(f"  ✓ Wrote golden_{question_id}.csv ({len(results)} rows)")
            except Exception as e:
                print(f"  ⚠ Failed to generate golden CSV: {e}")

            generated += 1

        print(f"\n✅ Generated {generated} test threads (skipped {skipped})")
        return generated, skipped
