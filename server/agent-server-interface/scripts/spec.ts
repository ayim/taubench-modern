import { readFileSync, writeFileSync } from "fs";

console.log("spec.ts starting...");

const args = process.argv.slice(2);
const source = args[0];
console.log("Source URL:", source);

if (!source) {
  console.error("No source provided");
  process.exit(1);
}

type Spec = {
  paths: Record<string, any>;
  servers: { url: string }[];
  info: { version: string };
};

const updatePaths = (spec: Spec, prefix: string): Spec => {
  console.log(`Adding prefix "${prefix}" to all paths.`);
  const updatedPaths: Record<string, any> = {};
  for (const [path, value] of Object.entries(spec.paths)) {
    const newPath = `${prefix}${path}`;
    updatedPaths[newPath] = value;
  }
  spec.paths = updatedPaths;
  delete spec.servers;
  return spec;
};

const preprocessAndGenerate = async () => {
  try {
    console.log("Fetching OpenAPI spec from:", source);
    const response = await fetch(source);
    console.log("Fetch response status:", response.status);
    const spec = (await response.json()) as Spec;
    console.log("Fetched spec keys:", Object.keys(spec));

    const pathPrefix = (() => {
      const server = spec.servers[0];
      if (!server) throw new Error("No server found in spec");
      return server.url;
    })();

    console.log("Modifying paths in the schema...");
    const updatedSpec = updatePaths(spec, pathPrefix);

    writeFileSync(
      "./src/spec.gen.ts",
      `/**
     * This file was auto-generated by ./scripts/spec.ts.
     * Do not make direct changes to the file.
     */
     export const spec = ${JSON.stringify(updatedSpec, null, 2).replaceAll(
       `"default": []`,
       `"default": [] as []`
     )};`
    );

    console.log("Wrote spec.gen.ts successfully.");

    const packageFile = readFileSync("./package.json", "utf-8");
    const packageJson = JSON.parse(packageFile);
    packageJson.version = spec.info.version;
    writeFileSync(
      "./package.json",
      JSON.stringify(packageJson, null, 2) + "\n"
    );
    console.log("Updated package.json with new version:", spec.info.version);
  } catch (err) {
    console.error("Error in spec.ts:", err);
    process.exit(1);
  }
};

preprocessAndGenerate();
