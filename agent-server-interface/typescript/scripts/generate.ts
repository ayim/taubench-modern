import fs from 'fs';
import generateTSTypes, { OpenAPI3, astToString } from 'openapi-typescript';

const args = process.argv.slice(2);

if (args.length === 0) {
  console.log('Usage: ts-node scripts/generate.ts <openapi-spec.json>');
  process.exit(1);
}

const openapiSpecFilepath = args[0];

// by default the automatically exported spec does not have agent server version prefix i.e. /api/v2
const appendServerPrefixToPaths = (spec: OpenAPI3): OpenAPI3 => {
  const server = spec.servers?.[0];
  if (!server) {
    throw new Error('No server found in spec');
  }
  const prefix = server.url;
  const updatedPaths: Record<string, any> = {};

  if (!spec.paths) {
    throw new Error('Missing paths in spec');
  }

  for (const [path, value] of Object.entries(spec.paths)) {
    const newPath = `${prefix}${path}`;
    updatedPaths[newPath] = value;
  }
  spec.paths = updatedPaths;
  delete spec.servers;
  return spec;
};

const generateSpecFile = (spec: OpenAPI3, outputFile: string) => {
  fs.writeFileSync(
    outputFile,
    `/**
   * This file was auto-generated by ./scripts/generate.ts.
   * Do not make direct changes to the file.
   */
   export const spec = ${JSON.stringify(spec, null, 2).replaceAll(
     `"default": []`,
     `"default": [] as []`
   )};`
  );
};

const generateSchemaFile = async (spec: OpenAPI3, outputFile: string) => {
  const generatedTypes = await generateTSTypes(spec);
  fs.writeFileSync(
    outputFile,
    `/**
   * This file was auto-generated by ./scripts/generate.ts.
   * Do not make direct changes to the file.
   */
  ${astToString(generatedTypes)}`
  );
};

const updatePackageJSON = (spec: OpenAPI3) => {
  const packageFile = fs.readFileSync('./package.json', 'utf-8');
  const packageJson = JSON.parse(packageFile);
  packageJson.version = spec.info.version;
  fs.writeFileSync(
    './package.json',
    JSON.stringify(packageJson, null, 2) + '\n'
  );
};

const main = async () => {
  console.log(`Generating schema for spec ${openapiSpecFilepath}...`);
  const spec = JSON.parse(
    fs.readFileSync(openapiSpecFilepath, 'utf-8')
  ) as OpenAPI3;
  const specWithPrefix = appendServerPrefixToPaths(spec);
  generateSpecFile(specWithPrefix, './src/private/spec.gen.ts');
  await generateSchemaFile(specWithPrefix, './src/private/schema.gen.ts');
  updatePackageJSON(specWithPrefix);
  console.log(`Schema generated successfully!`);
};

main();
